package Level1.Level1_StringToNum;
// Level 1 : 문자열을 정수로 바꾸기

class Solution {
    public int solution(String s) {
        return Integer.parseInt(s);
    }
}

public class Main {
    public static void main(String[] args) {
        Solution s = new Solution();
        System.out.println("1234");
        System.out.println("-1234");
    }
}
